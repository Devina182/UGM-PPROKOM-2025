def penjumlahan(a, b):
    return a + b
def pengurangan(a, b):
    return a - b
def perkalian(a, b):
    return a * b
def pembagian(a, b):
    if b == 0:
        return "Error pembagian dengan nol"
    return a / b
def modulo(a, b):
    if b == 0:
        return "Error tidak bisa modulo dengan nol"
    return a % b
def pangkat(a, b):
    return a ** b
